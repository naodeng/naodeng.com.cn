/**
 * Wiki Markdown structure cleaner (one-time batch, conservative)
 *
 * Usage:
 *   node scripts/wiki-markdown-cleaner.mjs --check
 *   node scripts/wiki-markdown-cleaner.mjs --write
 */
import fs from "fs";
import path from "path";

const WIKI_DIR = path.join(process.cwd(), "src", "content", "wiki");
const CHECK_ONLY = process.argv.includes("--check");
const WRITE = process.argv.includes("--write") || !CHECK_ONLY;

function parseFrontmatter(content) {
  if (!content.startsWith("---\n")) return null;
  const end = content.indexOf("\n---\n", 4);
  if (end === -1) return null;
  return {
    frontmatter: content.slice(0, end + 5),
    body: content.slice(end + 5),
  };
}

function repairTocMarkers(input, stats) {
  const lines = input.split(/\r?\n/);
  const startIdx = lines.findIndex((l) => l.includes("<!-- TOC START -->"));
  const endIdx = lines.findIndex((l) => l.includes("<!-- TOC END -->"));
  if (startIdx === -1 || endIdx !== -1) return input;

  let insertAt = lines.findIndex((l, i) => i > startIdx && /^##\s+/.test(l.trim()));
  if (insertAt === -1) insertAt = lines.length;

  lines.splice(insertAt, 0, "<!-- TOC END -->", "");
  stats.tocFixed += 1;
  return lines.join("\n");
}

function isFenceLine(line) {
  return /^\s*```/.test(line);
}

function isCodeLike(lines) {
  const joined = lines.join("\n");
  const codeSignals =
    (joined.match(
      /[{}();]|=>|\b(const|let|var|function|if|for|while|class|import|export|return|await|SELECT|FROM|WHERE)\b/g
    ) || []).length;
  return codeSignals >= 6;
}

function unwrapProseFences(input, stats) {
  const lines = input.split(/\r?\n/);
  const out = [];

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    const open = line.match(/^\s*```\s*(.*)$/);
    if (!open) {
      out.push(line);
      continue;
    }

    const lang = (open[1] || "").trim();
    let close = -1;
    for (let j = i + 1; j < lines.length; j += 1) {
      if (isFenceLine(lines[j])) {
        close = j;
        break;
      }
    }

    if (close === -1) {
      out.push(line);
      continue;
    }

    const block = lines.slice(i + 1, close);
    const bulletOrHeadingCount = block.filter((l) => /^\s*([-*]\s+|#{1,6}\s+)/.test(l)).length;

    const looksLikeProseBlock = !lang && bulletOrHeadingCount >= 3 && !isCodeLike(block);
    if (!looksLikeProseBlock) {
      out.push(line, ...block, lines[close]);
      i = close;
      continue;
    }

    // Unwrap and normalize indent (common 2-space indentation generated by previous tooling).
    const normalized = block.map((l) => (l.startsWith("  ") ? l.slice(2) : l));
    out.push(...normalized);
    stats.fencesUnwrapped += 1;
    i = close;
  }

  return out.join("\n");
}

function removeAdjacentDuplicateLines(input, stats) {
  const lines = input.split(/\r?\n/);
  const out = [];
  let inFence = false;

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    if (isFenceLine(line)) inFence = !inFence;

    const prev = out.length > 0 ? out[out.length - 1] : null;
    if (
      !inFence &&
      prev !== null &&
      prev.trim() !== "" &&
      line.trim() !== "" &&
      prev.trim() === line.trim()
    ) {
      stats.duplicateLinesRemoved += 1;
      continue;
    }
    out.push(line);
  }

  return out.join("\n");
}

function isListItemLine(line) {
  return /^\s*(?:[-*]|\d+\.)\s+/.test(line);
}

function normalizeListItem(line) {
  const trimmed = line.trim();
  if (/^\d+\.\s+/.test(trimmed)) {
    // Normalize ordered-list numbering so "1./2./3." blocks can be compared safely.
    return trimmed.replace(/^\d+\.\s+/, "1. ");
  }
  return trimmed.replace(/^[-*]\s+/, "- ");
}

function removeDuplicateListBlocks(input, stats) {
  const lines = input.split(/\r?\n/);
  let inFence = false;
  const blocks = [];

  let i = 0;
  while (i < lines.length) {
    if (isFenceLine(lines[i])) {
      inFence = !inFence;
      i += 1;
      continue;
    }
    if (inFence || !isListItemLine(lines[i])) {
      i += 1;
      continue;
    }

    const start = i;
    const items = [];
    while (i < lines.length && isListItemLine(lines[i])) {
      items.push(normalizeListItem(lines[i]));
      i += 1;
    }
    const end = i - 1;
    blocks.push({ start, end, items, key: items.join("\n").toLowerCase() });
  }

  const removeLine = new Set();
  for (let b = 0; b < blocks.length - 1; b += 1) {
    const a = blocks[b];
    if (a.items.length < 3) continue;

    for (let cIdx = b + 1; cIdx < blocks.length; cIdx += 1) {
      const c = blocks[cIdx];
      const gap = c.start - a.end;
      if (gap > 80) break;
      if (a.key !== c.key) continue;

      for (let lineNo = c.start; lineNo <= c.end; lineNo += 1) removeLine.add(lineNo);
      stats.duplicateBulletBlocksRemoved += 1;
    }
  }

  if (removeLine.size === 0) return input;
  return lines.filter((_, idx) => !removeLine.has(idx)).join("\n");
}

function normalizeBlankLinesAndEOF(input, stats) {
  const normalized = input.replace(/\r\n/g, "\n");
  const blankMatches = normalized.match(/\n{3,}/g) || [];
  if (blankMatches.length > 0) stats.blankRunsCollapsed += blankMatches.length;

  let out = normalized.replace(/\n{3,}/g, "\n\n").replace(/[ \t]+\n/g, "\n").replace(/\s*$/, "");
  out += "\n";
  return out;
}

function cleanContent(raw) {
  const parsed = parseFrontmatter(raw);
  if (!parsed) return null;

  const stats = {
    tocFixed: 0,
    fencesUnwrapped: 0,
    duplicateLinesRemoved: 0,
    duplicateBulletBlocksRemoved: 0,
    blankRunsCollapsed: 0,
  };

  let body = parsed.body;
  body = repairTocMarkers(body, stats);
  body = unwrapProseFences(body, stats);
  body = removeAdjacentDuplicateLines(body, stats);
  body = removeDuplicateListBlocks(body, stats);
  body = normalizeBlankLinesAndEOF(body, stats);

  return {
    content: parsed.frontmatter + body,
    stats,
  };
}

function main() {
  const files = fs.readdirSync(WIKI_DIR).filter((f) => f.endsWith(".md")).sort();
  const changed = [];
  const totals = {
    filesChanged: 0,
    tocFixed: 0,
    fencesUnwrapped: 0,
    duplicateLinesRemoved: 0,
    duplicateBulletBlocksRemoved: 0,
    blankRunsCollapsed: 0,
  };

  for (const file of files) {
    const fullPath = path.join(WIKI_DIR, file);
    const raw = fs.readFileSync(fullPath, "utf-8");
    const cleaned = cleanContent(raw);
    if (!cleaned) continue;

    if (cleaned.content !== raw) {
      totals.filesChanged += 1;
      totals.tocFixed += cleaned.stats.tocFixed;
      totals.fencesUnwrapped += cleaned.stats.fencesUnwrapped;
      totals.duplicateLinesRemoved += cleaned.stats.duplicateLinesRemoved;
      totals.duplicateBulletBlocksRemoved += cleaned.stats.duplicateBulletBlocksRemoved;
      totals.blankRunsCollapsed += cleaned.stats.blankRunsCollapsed;
      changed.push({ file, ...cleaned.stats });
      if (WRITE) fs.writeFileSync(fullPath, cleaned.content, "utf-8");
    }
  }

  console.log(`Wiki markdown cleaner mode: ${CHECK_ONLY ? "check" : "write"}`);
  console.log(`Changed files: ${totals.filesChanged}`);
  console.log(
    `Fix summary: TOC=${totals.tocFixed}, fences=${totals.fencesUnwrapped}, duplicateLines=${totals.duplicateLinesRemoved}, duplicateBlocks=${totals.duplicateBulletBlocksRemoved}, blankRuns=${totals.blankRunsCollapsed}`
  );
  if (changed.length > 0) {
    console.log("\nTop changed files:");
    changed.slice(0, 20).forEach((x) => {
      console.log(
        `- ${x.file}: toc=${x.tocFixed}, fences=${x.fencesUnwrapped}, dupLines=${x.duplicateLinesRemoved}, dupBlocks=${x.duplicateBulletBlocksRemoved}, blankRuns=${x.blankRunsCollapsed}`
      );
    });
  }

  if (CHECK_ONLY && totals.filesChanged > 0) {
    process.exitCode = 1;
  }
}

main();
